#include "libft.h"

void	ft_putnr(int n)
{
	if (n == (-2147483648))
		ft_putstr("-2147483648");
	if (n < 0)
	{
		ft_putchar('-');
		n = n * -1;
	}
	if (n >= 10)
	{
		ft_putnr(n / 10);
		ft_putchar((n % 10) + '0');
	}
	else
	{
		ft_putchar(n + '0');
	}  
}

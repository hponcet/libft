#include "libft.h"

int     ft_strcmp(const char *s1, const char *s2)
{
    size_t		i;
	int			ret;
    i = 0;
	while (s1[i] == s2[i])
	{
		if (s1[i] == '\0' && s2[i] == '\0')
			return (0);
		i++;
	}
	ret = (int)s1[i] - (int)s2[i];
	return (ret);
}

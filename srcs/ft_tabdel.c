#include "libft.h"

void		ft_tabdel(char ***table)
{
	int		i;
	
	i = 0;
	while (table[0][i])
	{
		ft_strdel(&table[0][i]);
		i++;
	}
	free(table[0]);
	table[0] = NULL;
}
